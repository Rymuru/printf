#include "ft_printf.h"

int    ft_bank(int nb)
{
    int     i;
    char    *stock = "0123456789ABCDEF";

    i = 0;
    while (nb > 0)
    {
        i++;
        if (stock[i] == 0)
            i = 0;
        nb--;
    }
    i = stock[i];
    return (i);
}